def update_symbols_with_arabic():
    filename = 'saudi_symbols.json'
    try:
        with open(filename, 'r', encoding='utf-8') as f:
            symbols_map = json.load(f)
            
        updated_count = 0
        
        for code, arabic_name in ARABIC_NAMES.items():
            if code in symbols_map:
                # Update name
                symbols_map[code]['name'] = arabic_name
                updated_count += 1
            else:
                # Add if missing? (Argaam might have newer/different list)
                # Let's add it, assuming standard SA symbol format
                symbols_map[code] = {
                    "symbol": code,
                    "yf_symbol": f"{code}.SR",
                    "name": arabic_name
                }
                updated_count += 1

        # Also localize TASI
        if 'TASI' in symbols_map:
             symbols_map['TASI']['name'] = "المؤشر العام (تاسي)"

        with open(filename, 'w', encoding='utf-8') as f:
            json.dump(symbols_map, f, ensure_ascii=False, indent=2)
            
        print(f"Updated {updated_count} symbols with Arabic names.")
        
    except FileNotFoundError:
        print("saudi_symbols.json not found.")

if __name__ == "__main__":
    update_symbols_with_arabic()
